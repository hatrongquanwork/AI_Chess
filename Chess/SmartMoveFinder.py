def findMoveNegaMax(gs, validMoves, depth, turnMultiplier):
    global nextMove
    if depth == 0:
        return turnMultiplier * scoreBoard(gs)
    maxScore = -CHECKMATE
    for move in validMoves:
        gs.makeMove(move)
        nextMoves = gs.getValidMoves()
        score = -findMoveNegaMax(gs, nextMoves, depth - 1, -turnMultiplier)
        if score > maxScore:
            maxScore == score
            if depth == DEPTH:
                nextMove = move
        gs.undoMove()
    return maxScore
def findMoveAlphaBetaPuring(gs, validMoves, depth, alpha, beta, turnMultiplier):
    global nextMove
    if depth == 0:
        return turnMultiplier * scoreBoard(gs)
    maxScore = -CHECKMATE
    for move in validMoves:
        gs.makeMove(move)
        nextMoves = gs.getValidMoves()
        score = -findMoveAlphaBetaPuring(gs, nextMoves, depth - 1, -alpha, -beta, -turnMultiplier)
        if score > maxScore:
            maxScore == score
            if depth == DEPTH:
                nextMove = move
        gs.undoMove()
        if maxScore > alpha:
            alpha = maxScore
        if alpha >= beta:
            break
    return maxScore

def scoreBoard(gs):
    if gs.checkMate:
        if gs.whiteToMove:
            return -CHECKMATE
        else:
            return CHECKMATE
    elif gs.staleMate:
        return STALEMATE

    score = 0
    for row in range(len(gs.board)):
        for col in range(len(gs.board[row])):
            square = gs.board[row][col]
            if square != '--':
                piecePS = 0
                if square[1] != "K":
                    if square[1] == "p":
                        piecePS = piecePositionScore[square][row][col]
                    else:
                        piecePS = piecePositionScore[square[1]][row][col]

                if square[0] == 'w':
                    score += pieceScore[square[1]] + piecePS * .1
                elif square[0] == 'b':
                    score -= pieceScore[square[1]] + piecePS * .1

    return score